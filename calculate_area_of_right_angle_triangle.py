"""
Find the area of a right-angled triangle, where 2 perpendiculars are known
If you know the two perpendicular sides (legs) (a) and (b):
Area = (1/2) * a * b
"""

a = float(input("Enter the value of side a: "))
b = float(input("Enter the value of side b: "))
area = 0.5 * a * b
print("Area of a Right Angle Triangle: ", area)